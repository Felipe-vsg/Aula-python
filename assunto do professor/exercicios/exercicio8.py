# Faça um programa que peça 2 números
# e retorne a soma, multiplicação, divisão, subtração


num1 = int (input ("Informe primeiro numero :"))
num2 = int (input ("Informe o segundo numero :"))


print (f"Soma deles é :{num1+num2}.")
print (f"Multiplicaçao deles é :{num1*num2}.")
print (f"Dvisão deles é :{num1/num2}.")
print (f"Subtração deles é :{num1-num2}.")